"""
К-удивительные числа
(Время: 3 сек. Память: 16 Мб Сложность: 25%)
Переворотом числа X назовем число, в котором все цифры числа X стоят в обратном порядке.
Например, переворотом числа 6372 является число 2736, а числа 7800 - 87.
Назовем K-удивительным такое число, которое в сумме со своим переворотом дает число K.

Например, у числа 222 имеется всего два K-удивительных числа:
111 и 210, а у числа 1050 имеется девять K-удивительных числа: 129, 228, 327, 426, 525, 624, 723, 822, 921.

Требуется написать программу, которая по заданному K определит количество K-удивительных чисел.

Входные данные
Входной файл INPUT.TXT содержит одно натуральное число K (1 ≤ K ≤ 106).

Выходные данные
Выходной файл OUTPUT.TXT должен содержать одно число - количество K-удивительных чисел.
"""


def reverse_number(x):
    """Возвращает переворот числа x (число с цифрами в обратном порядке)."""
    return int(str(x)[::-1])


def count_k_amazing_numbers(K):
    count = 0
    # Перебираем все возможные числа x, которые могут быть K‑удивительными
    # Логично ограничить перебор: x не может быть больше K (иначе x + reverse(x) > K)
    for x in range(1, K + 1):
        reversed_x = reverse_number(x)
        if x + reversed_x == K:
            count += 1
    return count


# Чтение входных данных
K = int(input().strip())

# Вычисление результата
result = count_k_amazing_numbers(K)

# Вывод выходных данных
print(result)
