"""
Перестановкой порядка n называется последовательность из попарно различных целых положительных чисел p1, p2, ... , pn,
где каждое 1 ≤ pi ≤ n. Будем говорить, что перестановка q1, q2, ... , qn лексикографически меньше перестановки p1, p2,
. . . , pn, если существует такое i, что qi < pi, а для любого j < i pj = qj .

Циклическим сдвигом на k перестановки p1, p2, ... , pn называется последовательность, pk+1, pk+2, ... , pn, p1, ... ,
pk. Исходная перестановка также считается своим циклическим сдвигом.
Отметим, что любой циклический сдвиг перестановки также является перестановкой.

Ваша задача состоит в том, чтобы найти наименьший лексикографически циклический сдвиг заданной перестановки.
"""
n = int(input())
p_n = list(map(int, input().split()))
a = p_n.index(min(p_n))
print(*p_n[a:], *p_n[0:a], sep=' ')
