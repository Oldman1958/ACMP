"""
TopCoder
(Время: 1 сек. Память: 16 Мб Сложность: 25%)
Некоторые из вас, наверное, слышали о сайте http://www.topcoder.com,
на котором часто проводятся различные соревнования по программированию.

В некоторых из них участникам предлагаются три задачи, каждая из которых оценивается в некоторое количество баллов.
В зависимости от того, насколько долго участник решал задачу, количество полученных им за нее баллов уменьшается.
Как и в большинстве других соревнований, выигрывает участник, набравший наибольшее число баллов.
Участники, набравшие одинаковое число баллов,
считаются выступившими одинаково и их порядок в таблице итоговых результатов не важен.
Из-за некоторых особенностей этих соревнований для предотвращения жульничества участники разделены в группы
по 20 человек, называемые комнатами.

Ваша задача заключается в том, чтобы написать программу,
которая по итоговым результатам в каждой комнате выводила бы суммарные итоговые результаты.

Входные данные
Первая строка входного файла INPUT.TXT содержит целое число n (1 ≤ n ≤ 100) - число комнат.
Далее следуют n описаний итоговых результатов в комнатах.

Результаты в i-ой комнате заданы в следующем формате.
Первая строка содержит целое число ni (1 ≤ ni ≤ 20)- количество участников в i-ой комнате.
Следующие ni строк содержат информацию о выступлениях участников.
j+1-ая строка описания результатов в i-ой комнате содержит информацию об участнике,
занявшем в i-ой комнате j-ое место:
разделенные одним пробелом вещественное число totalij (-5000 ≤ totalij ≤ 10000)
и строку nameij - соответственно количество набранных участником баллов и его имя.
Имя участника имеет длину от 1 до 25 и может содержать только буквы английского алфавита, цифры и символ подчеркивания.
При этом первый символ имени не является цифрой. Все вещественные числа заданы с двумя знаками после десятичной точки.

Гарантируется, что в каждой комнате участники упорядочены по невозрастанию набранных ими баллов.

Выходные данные
На первой строке выходного файла OUTPUT.TXT выведите N - суммарное число участников.
На следующих N строках выведите информацию о выступлении участников.
(k+1)-ая строка описания суммарных результатов должна содержать информацию об участнике,
занявшем k-ое место:
разделенные одним пробелом вещественное число totalk с двумя знаками после десятичной точки
и строку namek - соответственно количество набранных участником баллов и его имя.

Не забудьте, что участники должны быть упорядочены по невозрастанию набранных ими баллов.
"""
# Чтение входных данных
with open('INPUT.TXT', 'r') as f:
    lines = f.readlines()

n = int(lines[0].strip())  # количество комнат
participants = []  # список всех участников

current_line = 1

# Обработка каждой комнаты
for _ in range(n):
    ni = int(lines[current_line].strip())  # число участников в комнате
    current_line += 1

    # Чтение участников комнаты
    for _ in range(ni):
        line = lines[current_line].strip()
        total, name = line.split(' ', 1)  # разделяем по первому пробелу
        total = float(total)
        participants.append((total, name))
        current_line += 1

# Сортировка всех участников по убыванию баллов
participants.sort(key=lambda x: x[0], reverse=True)

# Запись выходных данных
with open('OUTPUT.TXT', 'w') as f:
    # Общее число участников
    f.write(f"{len(participants)}\n")

    # Информация о каждом участнике
    for total, name in participants:
        # Форматируем число с двумя знаками после запятой
        f.write(f"{total:.2f} {name}\n")

"""
Пояснение алгоритма
Чтение входных данных

Открываем файл INPUT.TXT и считываем все строки в список lines.

Первая строка содержит число комнат n.

Обработка комнат

Для каждой комнаты:

Читаем число участников ni.

Для каждого из ni участников:

Считываем строку с баллами и именем.

Разделяем строку на total (баллы) и name (имя), используя split(' ', 1) — разделение только по первому пробелу (чтобы корректно обработать имена с пробелами внутри, хотя по условию их нет).

Преобразуем total в число с плавающей точкой.

Добавляем пару (total, name) в общий список участников.

Сортировка

Сортируем всех участников по убыванию баллов с помощью sort() с ключом lambda x: x[0] и параметром reverse=True.

Запись выходных данных

Открываем файл OUTPUT.TXT для записи.

Первая строка — общее число участников len(participants).

Для каждого участника:

Записываем его баллы, форматированные до двух знаков после запятой (f"{total:.2f}"), и имя.

Особенности реализации
Формат чисел: используется форматирование :.2f, чтобы гарантировать два знака после запятой, даже если число целое или имеет меньше знаков.

Обработка имён: предполагается, что имя не содержит пробелов (по условию задачи), поэтому разделение по первому пробелу корректно.

Эффективность: алгоритм работает за O(NlogN), где N — общее число участников (до 2000), что укладывается в ограничения по времени.

Пример работы
INPUT.TXT:

2
3
950.00 Alice
800.50 Bob
700.00 Charlie
2
900.00 David
850.25 Eve
OUTPUT.TXT:

5
950.00 Alice
900.0 Newton
850.25 Eve
800.50 Bob
700.00 Charlie
"""