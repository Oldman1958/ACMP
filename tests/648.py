"""
Как-то раз Шрек решил посетить казино. Не будучи заядлым любителем азартных игр, Шрек обнаружил, что он не знает правил
ни одной из игр, доступных в казино. Недолго думая, Шрек решил все-таки поиграть. Его взор привлекла игра с довольно
незамысловатыми правилами.

На игровом столе лежат N карточек. На каждой карточке написано целое положительное число. Игра проходит между игроком и
крупье. Карточки лежат на столе числами вниз. Игра заключается в том, что игрок открывает ровно N/2 карточек.
Сумма всех чисел, написанных на карточках открытых игроком, называется “суммой игрока”. Следующим ходом крупье открывает
оставшиеся N/2 карточек. Сумма всех чисел, написанных на карточках открытых крупье, называется “суммой крупье”.
Выигрыш игрока определяется разностью чисел между “суммой игрока” и “суммой крупье”.
Очевидно, что полученная разность может быть отрицательным числом.
Это свидетельствует о том, что игрок проиграл и должен казино соответствующую сумму.

Все бы ничего, но Шрек обладает способностью видеть надписи сквозь бумагу любой плотности. Ваша задача определить
максимальную сумму выигрыша, которую может получить Шрек с учетом того, что он видит все числа, написанные на карточках.
"""
n = int(input())
cards = list(map(int, input().split()))
cards.sort()
m = int(n / 2)
shrec = sum(cards[m:])
crup = sum(cards[:m])
prise = shrec - crup
print(prise)
